package lnyswz.hk.bean;

import java.math.BigDecimal;

public class HkmxLog {
	private int id;
	private String hksj;
	private BigDecimal hkje;
	private String lastLsh;
	private String logNo;
	private int sxkhId;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getHksj() {
		return hksj;
	}
	public void setHksj(String hksj) {
		this.hksj = hksj;
	}
	public BigDecimal getHkje() {
		return hkje;
	}
	public void setHkje(BigDecimal hkje) {
		this.hkje = hkje;
	}
	public String getLastLsh() {
		return lastLsh;
	}
	public void setLastLsh(String lastLsh) {
		this.lastLsh = lastLsh;
	}
	public String getLogNo() {
		return logNo;
	}
	public void setLogNo(String logNo) {
		this.logNo = logNo;
	}
	public int getSxkhId() {
		return sxkhId;
	}
	public void setSxkhId(int sxkhId) {
		this.sxkhId = sxkhId;
	}
	
}
